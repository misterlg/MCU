#include "cu50.h"

/*
*********************************************************************************************************
* 功  能：温度转电阻
* 形  参：温度值
* 返回值：电阻值
* 备  注：CU50
* 作  者：LG
* 日  期：2020/12/22
*********************************************************************************************************
*/

float Cu50_TtoR(float t)
{
    float r = 0;
    
    r = 50 + 0.214*t;
    
    return r;
}

/*
*********************************************************************************************************
* 功  能：电阻转温度
* 形  参：电阻值
* 返回值：温度值
* 备  注：CU50
* 作  者：LG
* 日  期：2020/12/22
*********************************************************************************************************
*/

float Cu50_RtoT(float r)
{
	float t = 0;
    
    t = (r-50)/0.214;
    
    return t;
}
